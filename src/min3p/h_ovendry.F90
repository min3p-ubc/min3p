real*8 function h_ovendry(w,m) 
      
      
implicit none 
      
real*8, intent(in)    :: w
      
real*8, intent(in)    :: m

real*8                :: p, &
                         w2, &
                         w3 

integer               :: i 
      
real*8, parameter      :: w0=0.9d0, &
                          r1=1.0d0, &
                          r2=2.0d0, &
                          r4=4.0d0, &
                          r18=18.0d0, &
                          rsmall = 1.0d-50 
w3 = w*w*w
w2 = w*w
h_ovendry = dlog(w+rsmall) - m*w + m*(m-r1)*w2/r4 - m*(m-r1)*(m-r2)*w3/r18

 
return
end