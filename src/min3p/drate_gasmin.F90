!c ----------------------------------------------------------------------
!c subroutine drate_gasmin
!c ----------------------
!c
!c compute rate for gas pressure approximation mineral
!c
!c sign convention: dissolution -
!c                  precipitation +
!c
!c written by:      Uli Mayer - November 11, 01
!c
!c last modified: 
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:  
!c ----------------------------------------------------------------------
  
      subroutine drate_gasmin(ratem,im,iv,aream,drtinc,tid)
 
      use parm
      use chem
      use gen
      use phys
 
      implicit none
      
      integer :: ig, ireac, im, iv, tid
      real*8 :: ratem, aream, drtinc, satminc, pres_n2, pres_n2inc,    &
                pres_tot, pres_tot_inc, aentry_loc
      
      !local variables
      integer :: izn
      real*8, parameter :: r0 = 0.0d0, r1 = 1.0d0
      
!c  initialize total reaction rate

      ratem = r0

!c check to see if control volume is unsaturated


      if (soilhydrfunc_field) then
        aentry_loc = aentry_vol(iv)
      else
        izn = mpropvs(iv)
        aentry_loc = aentry(izn)
      end if

      if (uvsnew(iv).lt.aentry_loc) then

!c  compute total gas pressure

        pres_tot = r0
        pres_tot_inc = r0
        do ig = 1,ng
          pres_tot = pres_tot + rgasatm*tkel(iv)*gnew(ig,iv)
          pres_tot_inc = pres_tot_inc + rgasatm*tkel(iv)*ginc(ig,tid)
          if(nameg(ig).eq.'n2(g)') then
            pres_n2 = rgasatm*tkel(iv)*gnew(ig,iv)
            pres_n2inc = rgasatm*tkel(iv)*ginc(ig,tid)
          end if
        end do
!c  calculate saturation index for current mineral
 

        satm(im,tid) = pres_tot/pres_atm
        satminc = pres_tot_inc/pres_atm

!c  compute total dissolution/precipitation rate for surface controlled
!c  dissolution/precipitation reactions

        ireac = iamd(im)

        ratem = -rated(ireac,tid)*aream*                               &
                 ((1 - satminc)*(pres_n2inc/(pres_n2inc+0.01))-        &
                 (1 - satm(im,tid))*(pres_n2/(pres_n2+0.01)))
        
        ratem = ratem/drtinc
                   
      end if

      return
      end
