!c ----------------------------------------------------------------------
!c subroutine assngaspc
!c -------------------
!c
!c search for gas pair and assign value for gas_pair(ig)  
!c
!c written by:      Rich Amos - June 23, 2003
!c
!c last modified:   Rich Amos - June 23, 2003
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c
!c common:
!c bbls.f   integer
!c          -------
!c          gas_pair(ig)    =  component number for
!c                           gas pair for each gas
!c
!c        logical
!c        ---------
!c        gas_species(ng,nx)    = .true.-> species ix has a common 
!c                                component with gas ig
!c
!c chem.f    integer
!c        -------        
!c         ng                  = number of gases
!c        nx                    = number of species
!c         iax(nx+1)           = row pointer array to 
!c                                stoichiometric coefficients of 
!c                                components in secondary aqueous
!c                                species
!c         jax(nx*nc)          = column pointer array to 
!c                                stoichiometric coefficients of 
!c                                components in secondary aqueous
!c                                species
!c
!c local    integer
!c         -------
!c         ig                = counter (gases)
!c         ix                = counter (secondary species)
!c         iend            = pointer (stoichiometric
!c                             coefficients)
!c         istart          = pointer (stoichiometric
!c                             coefficients)
!c
!c
!c----------------------------------------------------------------------
    subroutine assngasspc

    use bbls
    use gen
    use chem

    implicit none
    
    integer :: i, ig, ix, istart, iend


!c_bubbles determine if species has a gas component
    do ig=1,ng            
        do ix=1,nx
            istart = iax(ix)
            iend = iax(ix+1)-1
            do i=istart,iend
                if (jax(i).eq.gas_pair(ig)) then
                    gas_species(ig,ix)=.true.
                end if
            end do
        end do
    end do            
    return
    end 
