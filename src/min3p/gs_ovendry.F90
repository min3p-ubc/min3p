!c ----------------------------------------------------------------------
!c real*8 function gs term for oven dry 
!c ------------------------
!c ----------------------------------------------------------------------
real*8 function gs_ovendry(w,m) 
      
      
implicit none 
      
real*8, intent(in)    :: w
      
real*8, intent(in)    :: m

real*8                :: p, &
                         rk

integer               :: i 
      
real*8, parameter      :: w0=0.9d0, &
                          r1=1.0d0, &
                          r0=0.0d0
gs_ovendry = r0
p = m + r1

do i=0,100
  rk = real(i)
  gs_ovendry = gs_ovendry + (w**rk)/( p + rk )  
end do

gs_ovendry = gs_ovendry*(w**p)
 
return
end
