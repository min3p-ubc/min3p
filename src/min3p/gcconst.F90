!c ----------------------------------------------------------------------
!c subroutine gcconst
!c ------------------
!c
!c assign constants for geochemistry 
!c
!c written by:      Uli Mayer - May 15, 96
!c
!c last modified:   Uli Mayer - November 19, 96
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   -
!c
!c common:
!c chem.f:   real*8:
!c           ------- 
!c           adav               = coefficient for Davies equation     * +
!c           basit              = coefficient for SIT model           * +
!c                                  based on Grenthe et al. (2004)
!c                                  Appendix B, eqn B.1, B.2
!c           avog_const         = avogadro constant [1 mol^-1]        * +
!c           bdav               = coefficient for Davies equation     * +
!c           diel_const         = dielectric constant of water        * +
!c                                unitless, for 25 deg C
!c           ehfac              = factor for computation of Eh        * +
!c                                from pe 
!c           farad_const        = faraday constant [C mol^-1]         * +
!c           pa_atm             = conversion factor [Pa atm ^-1]      * +
!c           permit_const       = permittivity of free space          * +
!c                                [C V^-1 m^-1] or [C^2 J^-1 m^-1]
!c           pres_atm           = atmospheric pressure [atm]          * +
!c           rgasatm            = ideal gas constant                  * +
!c                                [liter atm/(deg K mole)]
!c           rgascal            = ideal gas constant                  * +
!c                                [cal/(deg K mole)]
!c           rgasjoule          = ideal gas constant                  * +
!c                                [J/(deg K mol)]
!c           rho_w              = desnity of water [kg m^-3]          * +
!c           tempcs             = standard temperature [deg C]        * +
!c           tempks             = standard temperature [deg K]        * +
!c           tconv              = temperature conversion factor       * +
!c
!c bbls.f    logical
!c          -------
!c           gas_bubbles        = .true. -> gas phase saturation is calculated      
!c                                  below the water table
!c
!c ----------------------------------------------------------------------

      subroutine gcconst

      use parm
      use chem
      use bbls

      implicit none

!c  temperature

      tconv = 273.15d0                    !conversion factor, modify from 273.16d0 to 273.15d0. 
                                          !The conversion factor rkelvin=273.15d0 in other codes
      tempcs = 25.0d0                     !degree Celsius
      tempks = tempcs+tconv               !degree Kelvin

!c  calories to Joules
      cal2jou = 4.1868d0      

!c  ideal gas constant

      rgasatm = 0.082054d0 
      rgascal = 1.987d-3
      rgasjoule = 8.3145d0                !8.3144621d0 will cause occillination in mgc file for some case, DSU 
      
      !Gas parameters used in Richard Amos's code
      !rgasatm = 0.0820578d0 
      !rgascal = 1.986265d-3
      !rgasjoule = 8.3144621d0

!c  avogadro constant

      avog_const =  6.022d+23

!c  faraday constant

      farad_const = 96487.0d0

!c  dielectric constant of water

      diel_const = 7.85d+1

!c  permittivity of free space

      permit_const = 8.854d-12

!c  factor for calculating Eh [mV] from pe = 1000 * 2.303 R/F

      ehfac = 1.9788033d-1

!c  define constants for Davies equation

      adav = 0.5115d0
      bdav = 0.24d0

!c  define constants for SIT (Specific Ion Interaction Theory) model
!c     Grenthe et al. (2004) Chemical Thermodynamics of Uranium, 
!c     Appendix B: suggested B*a_j = 1.5, A=0.5091
      asit = 0.5091
      basit = 1.50d0
      
!c  constants for degassing
!c_bubbles if gas bubbles then set atmospheric pressure in initcpgb    

      pres_atm = 1.0d0           !atm
      rho_w = 1000.0d0           !kg m^-3
      pa_atm = 1.01325d5         !Pa atm^-1

      return
      end
