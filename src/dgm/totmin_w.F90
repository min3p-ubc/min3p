!c ----------------------------------------------------------------------
!c subroutine totmin_w
!c --------------------
!c
!c compute total source/sink term towards water concentration
!c due to mineral dissolution-precipitation reactions or derivative thereof
!c
!c written by:      Sergi Molins - June 7, 2006
!c
!c adapted from totmin written by Uli Mayer
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   real*8:
!c           -------
!c           ratem(nm)          = absolute dissolution-precipitation  + -
!c                                rates of minerals 
!c                                [moles/(l bulk*day)]
!c           totm_w             = total source/sink term towards      * +
!c                                water concentration
!c                                due to mineral dissolution-
!c                                precipitation reactions
!c                                [moles/(l bulk*day)]
!c common:
!c chem.f:   real*8:
!c           -------
!c           xnum(nm*nc)        = stoichiometric coefficients of      + -
!c                                components in mineral
!c
!c           integer*4:
!c           ----------
!c           iam(nm+1)          = row pointer array to                + -
!c                                stoichiometric coefficients of
!c                                components in mineral
!c           jam(nm*nc)         = column pointer array to             + -
!c                                stoichiometric coefficients of
!c                                components in mineral
!c           nc                 = number of components including h2o  + -
!c           nm                 = number of minerals                  + -
!c
!c
!c local:    integer*4:
!c           ----------
!c           i                  = counter (stoichiometric
!c                                coefficients)
!c           ic                 = counter (components) 
!c           im                 = counter (minerals)
!c           istart             = pointer (stoichiometric 
!c                                coefficients)
!c           iend               = pointer (stoichiometric
!c                                coefficients)
!c
!c external: ---
!c ----------------------------------------------------------------------
  
      subroutine totmin_w(ratem,totm_w)
 
      use chem, only : nm, iam, jam, xnum, namec

      implicit none
      
      real*8 :: ratem, totm_w

      dimension ratem(*)
      
      integer :: i, ic, im, istart, iend

      totm_w = 0.d0
 
      do im = 1,nm

        istart = iam(im)
        iend = iam(im+1)-1
        do i = istart,iend
          ic = jam(i)

!c  skip h2o, since not a primary unknown
 
          if (namec(ic).eq.'h2o') then
            totm_w = totm_w+xnum(i)*ratem(im)
          end if
        end do

      end do

      return
      end
