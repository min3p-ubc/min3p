!c ----------------------------------------------------------------------
!c subroutine zero_r8
!c ------------------
!c
!c clear real*8 array
!c
!c written by:      Uli Mayer - May 6, 96
!c
!c last modified:   Uli Mayer - November 27, 96
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   real*8:
!c           -------
!c           r8(n1*n2*n3)       = array to be cleared                 + -
!c
!c           integer*4:
!c           ----------
!c           n1                 = first dimension of array            + -
!c           n2                 = second dimension of array           + -
!c           n3                 = third dimension of array            + -
!c
!c common:   -
!c
!c local:    real*8:
!c           -------
!c           r0                 = constant
!c
!c           integer*4:
!c           ----------
!c           nentries           = number of entries        
!c           i                  = counter (number of entries
!c
!c external: -
!c ----------------------------------------------------------------------
 
      subroutine zero_r8(r8,n1,n2,n3)
      
      implicit none 
      
      real*8 :: r8(*)
      integer :: n1, n2, n3
      
      integer :: i, nentries
      real*8 :: r0
      parameter (r0 = 0.0d0)
 
      nentries = n1*n2*n3

      do i=1,nentries
        r8(i) = r0
      end do

      return
      end
