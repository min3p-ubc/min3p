!c ----------------------------------------------------------------------
!c real*8 function fluxfs
!c ------------------------
!c
!c compute water flux for fully saturated conditions
!c
!c written by:      Uli Mayer - May 29, 96
!c
!c last modified:
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   real*8:
!c           -------
!c           hhead_i            = hydraulic head in control volume i  + -
!c           hhead_j            = hydraulic head in control volume j  + -
!c           cinfvs             = influence coefficient               + -
!c           fluxfs             = water flux at current interface     * +
!c
!c common:   -
!c           
!c local:    -
!c
!c external: -
!c ----------------------------------------------------------------------
 
      real*8 function fluxfs(hhead_i,hhead_j,cinfvs)
      
      implicit none
 
      real*8 :: hhead_i, hhead_j, cinfvs
 
!c  compute interfacial flux

      fluxfs = cinfvs*(hhead_j-hhead_i)
 
      return
      end
