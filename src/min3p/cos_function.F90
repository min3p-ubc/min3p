 real*8 function cos_function(average,amp_year,amp_day,time_max_year,time_max_day,time)            
      
      
 implicit none 
      
 real*8, intent(in)    ::  average
      
 real*8, intent(in)    ::  amp_year
 
 real*8, intent(in)    ::  amp_day
      
 real*8, intent(in)    ::  time
      
 real*8, intent(in)    ::  time_max_year
 
 real*8, intent(in)    ::  time_max_day
 
 real*8                :: term_day
 
 real*8                :: term_year  
      
 real*8, parameter     :: r1=1.0d0
 
 real*8, parameter     :: r2=2.0d0
 
 real*8, parameter     :: r365=365.0d0
 
 real*8, parameter     :: pi=3.14159265359d0 


term_year = r2*pi*((time-time_max_year)/r365)
term_day = r2*pi*(time-time_max_day)

cos_function = average + amp_year * dcos(term_year) + amp_day * dcos(term_day)                                 
 
return
end
