!c ----------------------------------------------------------------------
!c subroutine rsort
!c ----------------
!c
!c sort real*8 array entries in increasing numerical order
!c
!c written by:      Uli Mayer - June 26, 96
!c
!c last modified:   Uli Mayer - November 25, 96
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c 
!c                                                                    I O
!c passed:   real*8:
!c           -------
!c           rlist(n)           = array to be sorted                  + +
!c           n                  = number of entries to be sorted      + -
!c
!c common:   -
!c
!c local:    i                  = counter (sorting)
!c           ip1                = pointer (sorting)
!c           rtemp              = temporary storage for entry of 
!c                                rlist
!c           npairs             = number of pairs to compare
!c           done               = .true. -> no interchange on 
!c                                          last pass
!c           
!c
!c external: -
!c ----------------------------------------------------------------------

      subroutine rsort (rlist,n)

      implicit none

      logical done   
      integer :: n
      real*8 :: rlist(n)
      
      !local variables
      integer :: i, ip1, npairs
      real*8 :: rtemp

!c     print *,'enter rsort'

      npairs = n - 1
      done  = .false.

      do while (.not. done)             ! continue until no interchanges
        done = .true.
        do i = 1,npairs
          ip1 = i + 1
          if (rlist(i).gt.rlist(ip1)) then    ! interchange
            rtemp     = rlist(i)
            rlist(i)   = rlist(ip1)
            rlist(ip1) = rtemp
            done      = .false.
          endif
        enddo
        npairs = npairs - 1
      enddo

      return
      end
