      subroutine rstatlc(idev, tid)
 
      use parm
      use chem

      implicit none
      
      integer :: idev, tid
      
      integer :: i      

!c  general run specific statistics

      write(idev,'(/a)') 'general run specific statistics ...' 
      write(idev,'(72a/)')('-',i=1,72)
      write(idev,'(a,i10)')   &
     &      'total number of Newton iterations       = ',ittot_lc(tid)
!c     write(idev,'(a,i10)') 
!c    &      'number of failed time steps             = ',ntstpf_lc
      write(idev,'(a,i10/)')  &
     &      'total number of time steps              = ',ntstp_lc(tid)

      return
      end
