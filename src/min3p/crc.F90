!c -----------------------------------------------------------------------
!c real*8 function crc
!c ---------------------
!c
!c temperature correction for gases based on CRC handbook
!c
!c written by:      Rich Amos - April 21, 2004
!c
!c last modified:   
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c
!c passed:   real*8:
!c           -------
!c           a,b,c              = solubility coefficients as given in the CRC handbook                  
!c           tempk              = temperature [K]                     + -
!c
!c
!c
!c local:    real*8:
!c           -------
!c           r1                 = constant
!c           r100               = constant
!c           enat               = e
!c           rh2o               = number of moles H2O in 1 L of water
!c
!c
!c external: -
!c ----------------------------------------------------------------------
  
      real*8 function crc(a,b,c,tempk)
 
      implicit none
      real*8 :: a,b,c,tempk
      real*8 :: r1, r100, rh2o, enat
      
      parameter (r1 = 1.0d0)
      parameter (enat = 2.71828182845904509d0)
      parameter (r100 = 1.0d2)
      parameter (rh2o = 5.551d1)
      
      crc = (enat**(a + b/(tempk/r100)+c*dlog(tempk/r100)))*rh2o
 
      return
      end
